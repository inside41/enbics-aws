package com.enbics.vo;

public class MapVO {
	private int idtb_map;
	private String company;
	private double lat;
	private double lng;
	
	public int getIdtb_map() {
		return idtb_map;
	}
	public void setIdtb_map(int idtb_map) {
		this.idtb_map = idtb_map;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
}
